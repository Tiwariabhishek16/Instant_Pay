package com.third_party_api.demo.Api.ModelFiles;

public class BeneficiaryRegistrationData {

	
	private String id;
	private String name;
	private String account;
	private String ifsc;
	private String bank;
	private String branch;
	private String verificationDt;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getIfsc() {
		return ifsc;
	}
	public void setIfsc(String ifsc) {
		this.ifsc = ifsc;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getVerificationDt() {
		return verificationDt;
	}
	public void setVerificationDt(String verificationDt) {
		this.verificationDt = verificationDt;
	}
	
	
	
	
	
}

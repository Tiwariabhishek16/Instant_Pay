public class RemitterLimitDetails {
	private String maximumDailyLimit;
    private String consumedDailyLimit;
    private String availableDailyLimit;
    private String maximumMonthlyLimit;
    private String consumedMonthlyLimit;
    private String availableMonthlyLimit;
	public String getMaximumDailyLimit() {
		return maximumDailyLimit;
	}
	public void setMaximumDailyLimit(String maximumDailyLimit) {
		this.maximumDailyLimit = maximumDailyLimit;
	}
	public String getConsumedDailyLimit() {
		return consumedDailyLimit;
	}
	public void setConsumedDailyLimit(String consumedDailyLimit) {
		this.consumedDailyLimit = consumedDailyLimit;
	}
	public String getAvailableDailyLimit() {
		return availableDailyLimit;
	}
	public void setAvailableDailyLimit(String availableDailyLimit) {
		this.availableDailyLimit = availableDailyLimit;
	}
	public String getMaximumMonthlyLimit() {
		return maximumMonthlyLimit;
	}
	public void setMaximumMonthlyLimit(String maximumMonthlyLimit) {
		this.maximumMonthlyLimit = maximumMonthlyLimit;
	}
	public String getConsumedMonthlyLimit() {
		return consumedMonthlyLimit;
	}
	public void setConsumedMonthlyLimit(String consumedMonthlyLimit) {
		this.consumedMonthlyLimit = consumedMonthlyLimit;
	}
	public String getAvailableMonthlyLimit() {
		return availableMonthlyLimit;
	}
	public void setAvailableMonthlyLimit(String availableMonthlyLimit) {
		this.availableMonthlyLimit = availableMonthlyLimit;
	}

}
